FIRST RUN
|cffffffff
Please type /reload on your first run to resize the ui to your screen. 
|r

kBar (Kronos Action Bars)
|cffffffff
Nice lightweight actionbar enhancement to allow you to customise your action bars.
|r

kNote (Kronos Friend list Notes)
|cffffffff
Gives you the ability to add notes to people on your friends list and ignore list, great way to keep track of possible offenders.
|r

KGMM (Kronos GM Messenger)
|cffffffff
KGMM is exactly what its called; an instant messenger interface for your in game whispers. It was specially designed to not interfere with your busy interface but also to have the convenience of having a chat window for each user that you communicate with. 

Be sure to check your Key Bindings screen and look for KGMM's available actions.
|r
Useful Slash Commands:
/KGMA    		|cffffffff- (this window)|r
/KGMM			|cffffffff- Whisper Option Window|r
/KGMM history	|cffffffff- Whisper History Viewer|r
/KBAR    		|cffffffff- Action Bar Options|r
/KNOTE    		|cffffffff- Friends list Notes Options|r

Advanced Slash Commands:
/KGMM reset			|cffffffff- Reset all options to default.|r
/KGMM reset filters	|cffffffff- Reload all built in filter definitions.|r
/KGMM clear history	|cffffffff- Clear history.|r
]]

